const dictionaries = { en, zh } as const;

export type I18nKey = MessageKey;
export type Translator = (key: I18nKey, values?: Record<string, string | number>) => string;

export function detectLocale(): Locale {
  const stored = typeof window === 'undefined' ? null : window.localStorage.getItem('edgetier-locale');
  if (stored === 'en' || stored === 'zh') return stored;
  const language = typeof navigator === 'undefined' ? '' : navigator.language.toLowerCase();
  return language.startsWith('zh') ? 'zh' : 'en';
}

export function persistLocale(locale: Locale): void {
  if (typeof window !== 'undefined') window.localStorage.setItem('edgetier-locale', locale);
}

export function createTranslator(locale: Locale): Translator {
  const dictionary = dictionaries[locale];
  return (key, values) => interpolate(dictionary[key] ?? en[key], values);
}

function interpolate(template: string, values?: Record<string, string | number>): string {
  if (!values) return template;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key: string) => String(values[key] ?? ''));
}
